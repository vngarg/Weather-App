import 'package:flutter/material.dart';
import 'package:weathe_app/Bloc/location_bloc.dart';
import 'package:weathe_app/Bloc/provider_bloc.dart';
import 'package:weathe_app/UI/WeatherScreen.dart';
import 'package:weathe_app/Widgets/TextField.dart';

class YoYo extends StatelessWidget {
  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(
        title: Center(child: Text('Weather App')),
      ),
      body: HomeScreen(),
    );
  }
}

class HomeScreen extends StatefulWidget {
  @override
  _HomeScreenState createState() => _HomeScreenState();
}

class _HomeScreenState extends State<HomeScreen> {
  final location = TextEditingController();

  @override
  Widget build(BuildContext context) {

    final bloc = BlocProvider.of<LocationBloc>(context);

    return Container(
      child: Column(
        children: <Widget>[
          TextFields('Enter Your Location', location),
          Container(
            height: 16.0,
          ),
          // Row(
          //   mainAxisAlignment: MainAxisAlignment.spaceAround,
          //   children: <Widget>[
          //     Buttons(
          //       data: 'Get Weather',
          //       location: location.text,
          //     ),
          //     Buttons(data: 'Get Map', location: location.text),
          //   ],
          // ),
          RaisedButton(
        child: Text('Get Weather'),
        onPressed: () {
          bloc.sendLocation(location);
        },
      ),
          Container(
            height: 16.0,
            child: Divider(
              color: Colors.black,
            ),
          ),
          Expanded(
            child: Container(
              child: Center(
                child: Text('For Map'),
              ),
            ),
          ),
          Row(
            children: <Widget>[
              Container(
                width: MediaQuery.of(context).size.width * 0.75,
              ),
              Container(
                child: FloatingActionButton(
                  onPressed: () {
                    Navigator.push(
                      context,
                      MaterialPageRoute(builder: (context) => WeatherDisplay()),
                    );
                  },
                  child: Icon(Icons.navigate_next),
                ),
              ),
            ],
          ),
          Container(
            height: 16.0,
          )
        ],
      ),
    );
  }
}
